import re


def formatar_cnpjs(cnpjs):
    # Para cada CNPJ da lista, remove tudo que não for número
    cnpjs_formatados = [re.sub(r'\D', '', cnpj) for cnpj in cnpjs]
    
    # Imprimir cada CNPJ em uma nova linha, com vírgula após cada um
    for i, cnpj in enumerate(cnpjs_formatados):
        # Se não for o último CNPJ, coloca a vírgula
        if i < len(cnpjs_formatados) - 1:
            print(f'"{cnpj}",')
        else:
            print(f'"{cnpj}"')

